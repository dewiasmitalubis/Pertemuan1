fun main() {
    //Tipe system yang dinamis tidak memerlukan adanya deklarasi tipe data pada sebuah variabel
    var dynamicVar = 2
    println("contoh dynamic variabel = $dynamicVar")

    //Tipe system yang statis memerlukan adanya deklarasi tipe data pada sebuah variabel
    var staticVar: Char = 'A'
    println("contoh static variabel = $staticVar")
}